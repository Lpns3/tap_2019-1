//uso o export para que eu possa importar essa funcao em outro arquivo
//default - funcao padrao, pode ter somente uma por arquivo
export default function sum (a, b){
    return a + b;
}

export function subtraction (a, b){
    return a - b;
}

export function multiplication (a, b){
    return a * b;
}

export function division (a, b){
    return a / b;
}